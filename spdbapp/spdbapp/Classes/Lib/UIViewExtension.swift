import Foundation
import UIKit

public extension UIView{
    var x: CGFloat{
        
        return self.frame.origin.x
    }
    
    var y: CGFloat{
        return self.frame.origin.y
    }
    
    var width: CGFloat{
        return self.frame.width
    }
    
    var height: CGFloat{
        return self.frame.height
    }
    
 
    
    
